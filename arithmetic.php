<?php

// exercise 3.5., functions, part 1

function add($a, $b) {
    echo $a + $b;
}

function subtract($a, $b) {
    echo $a - $b;
}

function multiply($a, $b) {
    echo $a * $b;
}

function divide($a, $b) {
    echo $a / $b;
}

echo add(10, 5) . PHP_EOL;
echo subtract(10, 5) . PHP_EOL;
echo multiply(10, 5) . PHP_EOL;
echo divide(10, 5) . PHP_EOL;

